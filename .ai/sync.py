def is_relative_to(path, root):
    try:
        return os.path.commonpath([os.path.abspath(path), os.path.abspath(root)]) == os.path.abspath(root)
    except ValueError:
        return False


def display_path_for_abs(path):
    ap = os.path.abspath(path)
    if is_relative_to(ap, REPO):
        return rel_from_repo(ap)
    home = os.path.abspath(os.path.expanduser("~"))
    if is_relative_to(ap, home):
        return "~/%s" % os.path.relpath(ap, home).replace(os.sep, "/")
    return ap.replace(os.sep, "/")


def native_display_path(path):
    path = str(path)
    normalized = path.replace(os.sep, "/")
    if normalized.startswith("~/"):
        return display_path_for_abs(os.path.expanduser(path))
    expanded = os.path.expanduser(path)
    if os.path.isabs(expanded):
        return display_path_for_abs(expanded)
    return normalized


def native_abspath(path):
    path = str(path)
    expanded = os.path.expanduser(path)
    if os.path.isabs(expanded):
        return os.path.abspath(expanded)
    return abspath(path)


def normalize_repo_rel_path(path):
    path = str(path).strip().replace(os.sep, "/")
    if path.startswith("./"):
        path = path[2:]
    expanded = os.path.expanduser(path)
    if os.path.isabs(expanded):
        ap = os.path.abspath(expanded)
        if not is_relative_to(ap, REPO):
            raise ValueError("%s is outside this repo; demote repo generated paths only" % path)
        path = rel_from_repo(ap)
    path = path.replace(os.sep, "/")
    if path in ("", ".", "..") or path.startswith("../"):
        raise ValueError("%s is not a repo-relative generated path" % path)
    return path


def native_logical_path(path):
    path = native_display_path(path)
    if path.startswith("~/"):
        return path[2:]
    return path


