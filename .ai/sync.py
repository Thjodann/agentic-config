def manifest_entry(path, source, kind, target, mode="file", block=None, content_hash=None):
    entry = {
        "path": path,
        "source": source,
        "kind": kind,
        "target": target,
        "mode": mode,
    }
    if block:
        entry["block"] = block
    if content_hash:
        entry["hash"] = content_hash
    return entry


def git_tracked_paths():
    try:
        out = subprocess.check_output(
            ["git", "ls-files", "-z"], cwd=REPO, stderr=subprocess.DEVNULL)
    except Exception:
        return set()
    return set(p for p in out.decode("utf-8").split("\0") if p)


def filter_stealth_tracked_outputs(texts, copies, managed, entries):
    if not STEALTH_MODE:
        return
    tracked = git_tracked_paths()
    if not tracked:
        return

    skipped_paths = set()
    for p in sorted(list(texts.keys())):
        if p in tracked:
            skipped_paths.add(p)
            del texts[p]
            add_stealth_skip(
                "%s is tracked; stealth mode skipped generated output for this path." % p)
    for p in sorted(list(copies.keys())):
        if p in tracked:
            skipped_paths.add(p)
            del copies[p]
            add_stealth_skip(
                "%s is tracked; stealth mode skipped generated output for this path." % p)
    for p in sorted(list(managed.keys())):
        if p in tracked:
            skipped_paths.add(p)
            del managed[p]
            if p == "AGENTS.md":
                add_stealth_skip(
                    "AGENTS.md is tracked; stealth mode skipped Codex managed rules "
                    "for this file. Use normal init or manual adoption for full Codex fidelity.")
            else:
                add_stealth_skip(
                    "%s is tracked; stealth mode skipped managed output for this path." % p)

    if skipped_paths:
        entries[:] = [
            e for e in entries
            if e.get("path") not in skipped_paths
        ]


def read_demotions():
    if not os.path.isfile(DEMOTIONS):
        return []
    try:
        data = json.load(open(DEMOTIONS, encoding="utf-8"))
    except Exception as e:
        raise SystemExit("ERROR: could not read %s (%s)" % (DEMOTIONS_REL, e))
    if not isinstance(data, dict):
        raise SystemExit("ERROR: %s must contain a JSON object" % DEMOTIONS_REL)
    records = data.get("demotions", [])
    if not isinstance(records, list):
        raise SystemExit("ERROR: %s demotions must be a list" % DEMOTIONS_REL)
    normalized = []
    seen = set()
    for record in records:
        if not isinstance(record, dict):
            raise SystemExit("ERROR: %s contains a non-object demotion" % DEMOTIONS_REL)
        consumer = record.get("consumer")
        path = record.get("path")
        if consumer not in TARGETS or not path:
            raise SystemExit("ERROR: %s contains an invalid demotion" % DEMOTIONS_REL)
        item = dict(record)
        item["path"] = normalize_repo_rel_path(path)
        key = (consumer, item["path"])
        if key in seen:
            continue
        seen.add(key)
        normalized.append(item)
    return sorted(normalized, key=lambda x: (x.get("consumer"), x.get("path")))


def write_demotions(records):
    records = sorted(records, key=lambda x: (x.get("consumer"), x.get("path")))
    if not records:
        if os.path.isfile(DEMOTIONS):
            os.remove(DEMOTIONS)
        return
    content = json.dumps(
        {"version": DEMOTIONS_VERSION, "demotions": records},
        indent=2, ensure_ascii=False, sort_keys=True) + "\n"
    write_text(DEMOTIONS, content)


def generated_output_group(path, entries):
    path = normalize_repo_rel_path(path)
    primary = None
    for entry in entries:
        if entry.get("mode", "file") == "file" and entry.get("path") == path:
            primary = entry
            break
    if not primary:
        return []

    group = [primary]
    if path.endswith("/SKILL.md"):
        base = path[:-len("/SKILL.md")]
        group = [
            entry for entry in entries
            if entry.get("mode", "file") == "file"
            and entry.get("source") == primary.get("source")
            and entry.get("target") == primary.get("target")
            and entry.get("kind") == primary.get("kind")
            and (entry.get("path") == path or entry.get("path", "").startswith(base + "/"))
        ]
    return sorted(group, key=lambda x: x.get("path"))


def apply_demotions(texts, copies, entries):
    demotions = read_demotions()
    if not demotions:
        return
    suppressed = set()
    for record in demotions:
        group = generated_output_group(record["path"], entries)
        if group:
            suppressed.update(entry["path"] for entry in group)
        else:
            suppressed.update(record.get("group") or [record["path"]])
    for path in suppressed:
        texts.pop(path, None)
        copies.pop(path, None)
    entries[:] = [
        entry for entry in entries
        if entry.get("path") not in suppressed
    ]


def build(assets, apply_saved_demotions=True):
    names = {}
    for asset in assets:
        key = asset["name"]
        if key in names:
            raise SystemExit(
                "ERROR: duplicate asset name '%s' (%s and %s). Names must be "
                "unique across rules, agents, commands, and skills." %
                (key, names[key], asset["type"]))
        names[key] = asset["type"]

    texts, copies, entries, degradations = {}, {}, [], []

    def claim(path, where):
        if path in texts or path in copies:
            raise SystemExit("ERROR: output collision at '%s' (from %s)." %
                             (path, where))

    for asset in assets:
        for target in TARGETS:
            if not target_allowed(asset, target):
                continue
            ot, oc, od = outputs_for(asset, target)
            for p, content in sorted(ot.items()):
                claim(p, asset["srcrel"])
                texts[p] = content
                entries.append(manifest_entry(
                    p, asset["srcrel"], asset["type"], target,
                    content_hash=sha256_text(content)))
            for p, src in sorted(oc.items()):
                claim(p, asset["srcrel"])
                copies[p] = src
                entries.append(manifest_entry(
                    p, asset["srcrel"], asset["type"], target,
                    content_hash=sha256_bytes(read_bytes(src))))
            degradations.extend(od)

    codex_rules = [
        a for a in assets
        if a["type"] == "rule" and target_allowed(a, "codex")
    ]
    managed = {}
    if codex_rules:
        managed["AGENTS.md"] = {RULES_BLOCK: rule_managed_block(codex_rules)}
        entries.append(manifest_entry(
            "AGENTS.md", source_rel("rules"), "rule", "codex",
            mode="managed-block", block=RULES_BLOCK))

    if apply_saved_demotions:
        apply_demotions(texts, copies, entries)
    filter_stealth_tracked_outputs(texts, copies, managed, entries)
    entries.sort(key=lambda e: (e["path"], e.get("block", ""), e["source"]))
    return texts, copies, managed, entries, sorted(set(degradations))


def read_manifest_entries():
    if not os.path.isfile(MANIFEST):
        return []
    try:
        data = json.load(open(MANIFEST, encoding="utf-8"))
    except Exception:
        return []
    outputs = data.get("outputs", [])
    entries = []
    for item in outputs:
        if isinstance(item, str):
            entries.append(manifest_entry(item, "", "", "", mode="file"))
        elif isinstance(item, dict) and item.get("path"):
            entries.append(item)
    return entries


def manifest_content(entries):
    return json.dumps({"version": 2, "outputs": entries},
                      indent=2, ensure_ascii=False, sort_keys=True) + "\n"


def managed_block_text(block, content):
    return (MANAGED_BEGIN.format(block=block) + "\n" +
            MARKER.format(src=source_rel("rules")) + "\n\n" +
            content.rstrip("\n") + "\n" +
            MANAGED_END.format(block=block) + "\n")


def apply_managed_block(existing, block, content):
    begin = MANAGED_BEGIN.format(block=block)
    end = MANAGED_END.format(block=block)
    pattern = re.compile(
        r"\n?%s\n.*?\n%s\n?" % (re.escape(begin), re.escape(end)),
        re.DOTALL)
    replacement = managed_block_text(block, content) if content else ""
    if pattern.search(existing):
        def repl(match):
            if not replacement:
                return "\n" if match.group(0).startswith("\n") else ""
            prefix = "\n" if match.group(0).startswith("\n") else ""
            return prefix + replacement
        updated = pattern.sub(repl, existing)
    elif replacement:
        sep = "\n\n" if existing.rstrip("\n") else ""
        updated = existing.rstrip("\n") + sep + replacement
    else:
        updated = existing
    return re.sub(r"\n{4,}", "\n\n\n", updated).rstrip("\n") + ("\n" if updated else "")


def expected_managed_content(path, blocks):
    ap = abspath(path)
    existing = read_text(ap) if os.path.isfile(ap) else ""
    updated = existing
    for block, content in sorted(blocks.items()):
        updated = apply_managed_block(updated, block, content)
    return updated


def prune_empty_dirs(rel_paths, boundary=None):
    boundary = os.path.abspath(boundary or REPO)
    for rp in rel_paths:
        d = os.path.dirname(native_abspath(rp))
        while is_relative_to(d, boundary) and d != boundary and os.path.isdir(d):
            if os.listdir(d):
                break
            os.rmdir(d)
            d = os.path.dirname(d)


def manifest_files_by_path(entries):
    by_path = {}
    for entry in entries:
        if entry.get("mode", "file") == "file" and entry.get("path"):
            by_path[entry["path"]] = entry
    return by_path


def existing_matches_manifest_hash(rel, entry):
    if not entry or not entry.get("hash"):
        return False
    ap = abspath(rel)
    if not os.path.isfile(ap):
        return False
    return sha256_bytes(read_bytes(ap)) == entry.get("hash")


def assert_safe_file_mutation(rel, old_entry, action, represented=None):
    ap = abspath(rel)
    if not os.path.exists(ap):
        return
    if not os.path.isfile(ap):
        raise SafetyError(
            "refusing to %s %s because that path exists and is not a file" %
            (action, rel))
    if has_marker(rel) or existing_matches_manifest_hash(rel, old_entry):
        return
    # A markerless native whose content is already captured verbatim in `.ai/`
    # (an adopted source surface) is safe to refresh in place: the projection we
    # write is the canonical truth, so nothing is lost. Divergent or unrelated
    # markerless files are never in `represented`, so they stay protected.
    if represented and rel in represented:
        return
    raise SafetyError(
        "refusing to %s markerless file at generated path %s. "
        "Run %s status and resolve the Blocking item before syncing." %
        (action, rel, COMMAND_NAME))


def preflight_file_mutations(texts, copies, old_entries, new_file_paths, represented=None):
    old_files = manifest_files_by_path(old_entries)
    tracked = git_tracked_paths() if STEALTH_MODE else set()
    for p, content in sorted(texts.items()):
        ap = abspath(p)
        if os.path.isfile(ap) and read_text(ap) == content:
            continue
        assert_safe_file_mutation(p, old_files.get(p), "overwrite", represented)
    for p, src in sorted(copies.items()):
        ap = abspath(p)
        if os.path.isfile(ap) and read_bytes(ap) == read_bytes(src):
            continue
        assert_safe_file_mutation(p, old_files.get(p), "overwrite", represented)
    for entry in old_entries:
        if entry.get("mode", "file") != "file":
            continue
        p = entry.get("path")
        if p and p not in new_file_paths and os.path.exists(abspath(p)):
            if STEALTH_MODE and p in tracked:
                add_stealth_skip(
                    "%s is tracked; stealth mode skipped pruning stale generated output." % p)
                continue
            assert_safe_file_mutation(p, entry, "prune", represented)
    return old_files


def compare_outputs(texts, copies, managed, entries):
    drift = []
    tracked = git_tracked_paths() if STEALTH_MODE else set()
    for p, content in sorted(texts.items()):
        ap = abspath(p)
        if not os.path.isfile(ap) or read_text(ap) != content:
            drift.append(p)
    for p, src in sorted(copies.items()):
        ap = abspath(p)
        if not os.path.isfile(ap) or read_bytes(ap) != read_bytes(src):
            drift.append(p)
    for p, blocks in sorted(managed.items()):
        ap = abspath(p)
        expected = expected_managed_content(p, blocks)
        if not os.path.isfile(ap) or read_text(ap) != expected:
            drift.append("%s (managed block)" % p)

    old_entries = read_manifest_entries()
    new_file_paths = set(list(texts.keys()) + list(copies.keys()))
    new_managed = set((p, b) for p, blocks in managed.items() for b in blocks)
    for entry in old_entries:
        mode = entry.get("mode", "file")
        p = entry.get("path")
        if mode == "managed-block":
            key = (p, entry.get("block"))
            if key not in new_managed and os.path.isfile(abspath(p)):
                if STEALTH_MODE and p in tracked:
                    add_stealth_skip(
                        "%s is tracked; stealth mode skipped pruning stale managed output." % p)
                    continue
                existing = read_text(abspath(p))
                begin = MANAGED_BEGIN.format(block=entry.get("block"))
                if begin in existing:
                    drift.append("%s (stale managed block)" % p)
        elif p and p not in new_file_paths and os.path.exists(abspath(p)):
            if STEALTH_MODE and p in tracked:
                add_stealth_skip(
                    "%s is tracked; stealth mode skipped pruning stale generated output." % p)
                continue
            drift.append("%s (stale; should be pruned)" % p)

    cur_manifest = read_text(MANIFEST) if os.path.isfile(MANIFEST) else ""
    new_manifest = manifest_content(entries)
    if cur_manifest != new_manifest:
        drift.append(MANIFEST_REL)
    return drift


def write_outputs(texts, copies, managed, entries, represented=None):
    old_entries = read_manifest_entries()
    tracked = git_tracked_paths() if STEALTH_MODE else set()
    new_file_paths = set(list(texts.keys()) + list(copies.keys()))
    new_managed = set((p, b) for p, blocks in managed.items() for b in blocks)
    old_files = preflight_file_mutations(
        texts, copies, old_entries, new_file_paths, represented)
    created = updated = 0

    for p, content in sorted(texts.items()):
        ap = abspath(p)
        if os.path.isfile(ap) and read_text(ap) == content:
            continue
        assert_safe_file_mutation(p, old_files.get(p), "overwrite", represented)
        existed = os.path.exists(ap)
        write_text(ap, content)
        created += 0 if existed else 1
        updated += 1 if existed else 0

    for p, src in sorted(copies.items()):
        ap = abspath(p)
        data = read_bytes(src)
        if os.path.isfile(ap) and read_bytes(ap) == data:
            continue
        assert_safe_file_mutation(p, old_files.get(p), "overwrite", represented)
        existed = os.path.exists(ap)
        write_bytes(ap, data)
        created += 0 if existed else 1
        updated += 1 if existed else 0

    managed_updated = 0
    for p, blocks in sorted(managed.items()):
        ap = abspath(p)
        expected = expected_managed_content(p, blocks)
        if os.path.isfile(ap) and read_text(ap) == expected:
            continue
        write_text(ap, expected)
        managed_updated += 1

    pruned = []
    for entry in old_entries:
        mode = entry.get("mode", "file")
        p = entry.get("path")
        if not p:
            continue
        if mode == "managed-block":
            block = entry.get("block")
            if (p, block) not in new_managed and os.path.isfile(abspath(p)):
                if STEALTH_MODE and p in tracked:
                    add_stealth_skip(
                        "%s is tracked; stealth mode skipped pruning stale managed output." % p)
                    continue
                existing = read_text(abspath(p))
                updated_content = apply_managed_block(existing, block, "")
                if updated_content != existing:
                    write_text(abspath(p), updated_content)
                    pruned.append("%s (%s block)" % (p, block))
        elif p not in new_file_paths and os.path.exists(abspath(p)):
            if STEALTH_MODE and p in tracked:
                add_stealth_skip(
                    "%s is tracked; stealth mode skipped pruning stale generated output." % p)
                continue
            assert_safe_file_mutation(p, entry, "prune", represented)
            os.remove(abspath(p))
            pruned.append(p)
    prune_empty_dirs([p.split(" (", 1)[0] for p in pruned])

    new_manifest = manifest_content(entries)
    if not os.path.isfile(MANIFEST) or read_text(MANIFEST) != new_manifest:
        write_text(MANIFEST, new_manifest)

    return created, updated, managed_updated, pruned


def print_stealth_skips():
    if not STEALTH_SKIPS:
        return
    print("Stealth skipped tracked outputs:")
    for item in STEALTH_SKIPS:
        print("  %s" % item)


def command_sync(check=False):
    assets = load_assets()
    texts, copies, managed, entries, _degradations = build(assets)
    if check:
        drift = compare_outputs(texts, copies, managed, entries)
        if drift:
            print("Generated IDE folders are STALE vs %s master:" % AI_REL)
            for d in drift:
                print("    %s" % d)
            print("\nRun %s sync to regenerate local projections and refresh the manifest." % COMMAND_NAME)
            print("If sync is blocked, run %s status and resolve the Blocking items first." % COMMAND_NAME)
            print_stealth_skips()
            return 1
        print("In sync: %d generated entries match %s master." % (len(entries), AI_REL))
        print_stealth_skips()
        return 0

    represented = represented_native_paths(assets)
    try:
        created, updated, managed_updated, pruned = write_outputs(
            texts, copies, managed, entries, represented)
    except SafetyError as e:
        print("ERROR: %s" % e)
        print("")
        print("Sync is blocked by a markerless native file that Agentic Config will not overwrite.")
        print("Run %s status to see blocking items and the next safe step." % COMMAND_NAME)
        return 1
    by_type = {}
    for asset in assets:
        by_type[asset["type"]] = by_type.get(asset["type"], 0) + 1
    summary = ", ".join("%d %ss" % (by_type[k], k) for k in sorted(by_type))
    print("Synced %d canonical assets (%s) -> %d manifest entries across %d IDEs." %
          (len(assets), summary, len(entries), len(TARGETS)))
    print("  created %d, updated %d, managed %d, pruned %d." %
          (created, updated, managed_updated, len(pruned)))
    for p in pruned:
        print("  pruned %s" % p)
    print_stealth_skips()
    return 0


def has_marker(rel):
    ap = native_abspath(rel)
    if not os.path.isfile(ap):
        return False
    try:
        return "AUTOGENERATED from" in read_text(ap)
    except UnicodeDecodeError:
        return False


def detect_native(ide, rel):
    rel = native_logical_path(rel)
    if ide == "cursor":
        if rel.startswith(".cursor/rules/") and rel.endswith(".mdc"):
            return "rule"
        if rel.startswith(".cursor/commands/") and rel.endswith(".md"):
            return "command"
        if rel.startswith(".cursor/agents/") and rel.endswith(".md"):
            return "agent"
        if rel.startswith(".cursor/skills/") and rel.endswith("/SKILL.md"):
            return "skill"
    if ide == "windsurf":
        if (rel.startswith(".devin/rules/") or rel.startswith(".windsurf/rules/")) and rel.endswith(".md"):
            return "rule"
        if rel.startswith(".windsurf/workflows/") and rel.endswith(".md"):
            return "command"
        if rel.startswith(".windsurf/skills/") and rel.endswith("/SKILL.md"):
            return "skill"
    if ide == "codex":
        if rel.startswith(".codex/rules/") and rel.endswith(".rules"):
            return "unsupported-policy"
        if rel.startswith(".agents/skills/") and rel.endswith("/SKILL.md"):
            return "skill"
        if rel.startswith(".codex/skills/") and rel.endswith("/SKILL.md"):
            return "skill"
        if rel.startswith(".codex/agents/") and rel.endswith(".toml"):
            return "agent"
    if ide == "claude":
        if rel.startswith(".claude/rules/") and rel.endswith(".md"):
            return "rule"
        if rel.startswith(".claude/commands/") and rel.endswith(".md"):
            return "command"
        if rel.startswith(".claude/agents/") and rel.endswith(".md"):
            return "agent"
        if rel.startswith(".claude/skills/") and rel.endswith("/SKILL.md"):
            return "skill"
    if ide == "continue":
        if rel.startswith(".continue/prompts/") and rel.endswith(".md"):
            return "command"
    return None


