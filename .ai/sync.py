def strip_marker(body):
    lines = body.splitlines()
    lines = [ln for ln in lines if "AUTOGENERATED from" not in ln]
    return "\n".join(lines).strip("\n") + "\n"


def parse_simple_toml_agent(text, src):
    values = {}
    lines = text.splitlines()
    i = 0
    while i < len(lines):
        line = lines[i].strip()
        i += 1
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, val = line.split("=", 1)
        key, val = key.strip(), val.strip()
        if val.startswith('"""'):
            collected = [val[3:]]
            while i < len(lines) and not collected[-1].endswith('"""'):
                collected.append(lines[i])
                i += 1
            joined = "\n".join(collected)
            if joined.endswith('"""'):
                joined = joined[:-3]
            values[key] = joined
        else:
            try:
                values[key] = json.loads(val)
            except Exception:
                values[key] = strip_quotes(val)
    body = values.get("developer_instructions") or values.get("prompt") or ""
    desc = values.get("description") or first_paragraph(body) or "Imported Codex agent."
    name = portable_asset_name(values.get("name") or slugify(src))
    fm = {"name": name, "description": desc, "model": values.get("model")}
    return base_asset("agent", name, desc, body, src, fm=fm)


