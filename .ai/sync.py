def base_asset(kind, name, description, body, srcrel, fm=None, srcdir=None):
    fm = fm or {}
    asset = {
        "type": kind,
        "name": fm.get("name") or name,
        "description": description,
        "body": body,
        "srcrel": srcrel,
        "targets": fm.get("targets"),
        "tools": fm.get("tools"),
        "model": fm.get("model"),
        "argument-hint": fm.get("argument-hint"),
        "allowed-tools": fm.get("allowed-tools"),
        "disallowed-tools": fm.get("disallowed-tools"),
        "activation": normalize_activation(fm.get("activation") or fm.get("trigger")),
        "globs": fm.get("globs") or fm.get("paths"),
        "aux": {},
    }
    if kind != "rule":
        asset["activation"] = None
        asset["globs"] = None
    if srcdir is not None:
        for root, _dirs, files in os.walk(srcdir):
            for f in files:
                if root == srcdir and f == "SKILL.md":
                    continue
                if f == ".DS_Store":
                    continue
                ap = os.path.join(root, f)
                rel = os.path.relpath(ap, srcdir).replace(os.sep, "/")
                asset["aux"][rel] = ap
    return asset


def load_markdown_asset(kind, name, path, srcrel, srcdir=None):
    text = read_text(path)
    fm, body = parse_frontmatter(text, srcrel, require=True)
    description = fm.get("description")
    if not description:
        raise ValueError("%s: missing required 'description'" % srcrel)
    return base_asset(kind, name, description, body, srcrel, fm=fm, srcdir=srcdir)


def load_assets():
    assets = []

    rdir = os.path.join(AI_DIR, "rules")
    for f in sorted(os.listdir(rdir)) if os.path.isdir(rdir) else []:
        if f.endswith(".md"):
            assets.append(load_markdown_asset(
                "rule", f[:-3], os.path.join(rdir, f), source_rel("rules", f)))

    adir = os.path.join(AI_DIR, "agents")
    for f in sorted(os.listdir(adir)) if os.path.isdir(adir) else []:
        if f.endswith(".md"):
            assets.append(load_markdown_asset(
                "agent", f[:-3], os.path.join(adir, f), source_rel("agents", f)))

    cdir = os.path.join(AI_DIR, "commands")
    for f in sorted(os.listdir(cdir)) if os.path.isdir(cdir) else []:
        if f.endswith(".md"):
            assets.append(load_markdown_asset(
                "command", f[:-3], os.path.join(cdir, f), source_rel("commands", f)))

    sdir = os.path.join(AI_DIR, "skills")
    for d in sorted(os.listdir(sdir)) if os.path.isdir(sdir) else []:
        skill_md = os.path.join(sdir, d, "SKILL.md")
        if os.path.isfile(skill_md):
            assets.append(load_markdown_asset(
                "skill", d, skill_md, source_rel("skills", d, "SKILL.md"),
                srcdir=os.path.join(sdir, d)))

    assets.sort(key=lambda a: (a["type"], a["name"]))
    return assets


